				<h1>7 dicas para diminuir sintomas de ansiedade</h1>
				Segundo a OMS (Organização mundial da Saúde) cerca de 24% dos brasileiros sofrem algum distúrbio de ansiedade, números que mostram uma realidade de muitos brasileiros que sofrem com a doença, que pode levar a danos irreparáveis ou até mesmo ao suicídio. Vários fatores podem levar o desenvolvimento da ansiedade como: Herança genética, doenças hormonais, problemas de infância, uso de álcool e drogas dentre outros.
				<br>
				A ansiedade se manifesta em sintomas físicos como: Respiração ofegante e falta de ar, tontura, enxaquecas, irritabilidade, tensão muscular, palpitações e dores no peito, fala acelerada, agitação de pernas e braços dentre outros. 
				<br>
				 Citaremos dicas, hábitos e costumes que podem ajudar a diminuir a ansiedade, porém, deixando claro que nada substitui o acompanhamento de um profissional formado na área que possa dar suporte e um diagnóstico mais preciso sobre cada caso. 
				<br>
				<br>
				<h1>Tratando a Ansiedade</h1> 
				<br>
				Diversos hábitos e costumes ajudam na diminuição dos sintomas do transtorno de ansiedade. Conheça 7 desses hábitos e tente aplicar no seu cotidiano. 
				<br>
				<br>
				<b>1.Pense positivo </b>
				<br>
				<br>
				Grandes problemas do nosso dia-a-dia aparecem devido a pensamentos pessimistas, que nos colocam para baixo, nossos pensamentos influenciam diretamente na nossa visão de mundo e principalmente na nossa saúde física e mental. Então pense positivo. 
				<br>
				<br>
				<b>2.Tenha hábito de se exercitar </b>
				<br>
				<br>
				O hábito de se exercitar libera hormônios responsáveis pelo bem-estar como a dopamina, serotonina e a adrenalina. Uma pequena caminhada todas as manhãs pode trazer um ótimo resultado em um curto período de tempo. 
				<br>
				<br>
				<b>3.Durma bem</b> 
				<br>
				<br>
				 Um sono regulado é essencial para o nosso humor e disposição durante o dia, busque estar sempre descansado e não forçar muito o seu corpo em questão de poucas horas dormidas. Um corpo saudável significa mente saudável. 
				<br>
				<br>
				<b>4.Seja mais sociável </b>
				<br>
				<br>
				Por mais clichê que possa parecer, ter boas companhias durante o nosso dia e amigos em que podemos contar, ajuda a diminuir os sintomas da ansiedade além de trazer uma sensação de satisfação. 
				<br>
				<br>
				<b>5. Procure técnica de relaxamento e meditação </b>
				<br>
				<br>
				A tensão de um dia corrido de trabalho, do barulho no trânsito, do estresse dentro de casa contribui muito para o desenvolvimento da ansiedade. Procure momentos do dia para relaxar, meditar e aliviar a mente. Momentos de silêncio são fundamentais para uma boa saúde mental. 
				<br>
				<br>
				<b>6.Tenha um Hobbie</b> 
				<br>
				<br>
				Procure ocupar a sua mente com atividade que tragam satisfação para você, que te faça sentir bem e consiga te distrair dos diversos fatores que contribuem para a destruição da sua sanidade. 
				<br>
				<br>
				<b>7.Se alimente bem </b>
				<br>
				<br>
				As saúdes físicas e mentais estão interligadas e uma depende da outra. Sua mente não terá paz, se o seu corpo estiver doente, busque se alimentar bem e ingerir alimentos que sejam saudáveis. 
				<br>
				<br>
				<b>8.Procure um Profissional</b> 
				<br>
				<br>
				Todas as demais dicas diminuem os sintomas da ansiedade, produzem uma sensação de bem-estar maior, ajudam a lutar contra essa condição que faz o sofrimento de diversas pessoas. Porém um profissional capacitado irá saber tratar melhor tal condição e receitar remédios se preciso.   
